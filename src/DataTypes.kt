fun main(){
    var numberInt: Int = 10;
    var numberLong: Long = 10L;
    var numberDouble: Double = 2.3;
    var numberFloat: Float = 2.3F;
    var booleanType: Boolean = true;
    var stringType: String = "Vitor";
    var charType: Char = 'V';

    println("Number Int Type: ${numberInt}")
    println("Number Long Type: ${numberLong}")
    println("Number Long Type: ${numberDouble}")
    println("Number Float Type: ${numberFloat}")
    println("Boolean Type: ${booleanType}")
    println("String Type: ${stringType}")
    println("Char Type: ${charType}")
}